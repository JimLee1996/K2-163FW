function vendor_by_mac(mac) 
	local prefix = string.sub(string.upper(mac),1,8)
	for _, v in ipairs (devMac) do 
		if v.mac == prefix then
		    return v.vendor
		end
	end
end

function icon_by_vendor(vendor) 
    if vendor == nil then
        return "brand/unknown.png"
    else
        for _, v in ipairs (vendorPath) do 
            if v.vendor == vendor then
                return v.path
            end
        end
	end
end

--根据mac地址获取对应的厂商图标路径
function icon_path_by_mac(mac)
    local vendor_name
	vendor_name = vendor_by_mac(mac)
    return icon_by_vendor(vendor_name)
end
